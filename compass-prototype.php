  	<p class="text-left"><strong>[ INDIVIDUAL PROPOSAL ]</strong> The individual part of this project was to provide visuals that give a firm understanding of the overall look and feel of the final product. I treated it as if I were proposing this to a real client. The deliverables included style tiles, style guides, wireframes, a functioning prototype, and a pitch document explaining the philosophy behind the proposed user experience. I asked several people to pick which style tile they liked the most out of the 3 that I made and created a prototype from that. The style guide provided a visualization of fonts, layouts, voice and tone of the website.</p>
