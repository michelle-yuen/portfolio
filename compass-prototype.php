  	<h1>The Scope</h1>
  	<p class="text-left">Compass is Vancouver's reloadable fare card system that works everywhere on transit. The goal of this UX project was to redesign the current Compass Card website based on a content strategy proposal. The project was split into two parts. In the first part, my team and I came up with a content strategy. For the second part of the project, I provided deliverables for the look, feel, and functionality of the final site based on the proposed content stragey.</p>

  	<h1>The Process</h1>
  	<p class="text-left"><strong>[ CONTENT STRATEGY ]</strong> Working in a group of 5 people including myself, we came up with a content strategy on which part of the current Compass Card website to improve on. We started off with a design sprint in class to come up with ideas for what we wanted to focus on. In the end, we chose to focus on how to better communicate the necessary information to all transit users. After we had our direction for the project, we conducted user research on friends and family based on online surveys and in-person interviews. When we got the results back, we were able to come up with 3 fact-based personas and their corresponding empathy maps. From there we stated the project, stakeholder, and end-user goals and created a strategy which included things like a context map, content model, gap analysis, and information architecture. For more information on the content strategy, please view the PDF below.</p>

