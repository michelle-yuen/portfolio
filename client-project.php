  	<h1>The Scope</h1>
  	<p class="text-left">The Dreadquarters is a hair salon specializing in dreadlocks and alternative hair. They have extensive experience in the creation and long term care of â€˜locks, for clients who travel from all over North America to their shop. Our client Kris, who is the owner of the shop, took over the business 4 years ago and created their current website on a WordPress theme with no web design experience. Unfortunately, the site was showing its age and even though it still functioned, it was outdated and overly complex to maintain.</p>
    <p class="text-left">Kris prides herself on The Dreadquarters' works and she wanted the galleries to be the main focus of the website so that clients could easily access the images. My team of 4 including myself contacted Kris through our Technical Web Designer program at BCIT to arrange an intial in-person meeting with her to better understand her needs and goals for the website. The project was completed in 1.5 months and served as one of our final projects.</p>

  	<h1>The Process</h1>
  	<p class="text-left"><strong>[ CONTENT, IA, WIREFRAMES ] </strong>After our initial meeting with Kris, we had a pretty good understanding of how the website would be laid out. Our client had provided us with a website that she liked for reference so we got an idea of what she liked. Her requirements were clear and direct, and we got straight to the wireframes but we really should have began with the information architecture in order to structure the content first. Fortunately, this did not have a major impact for us, but for a larger website it would have been costly.</p>

    <p class="text-left">In general, our client wanted to simplify the whole website by getting rid of the drop-down menu and having users be able to find most things without having to click around too many pages. She also had a blog that was not active and had broken images which she wanted to keep on the main menu, so we suggested to keep it but pick and choose the more relevant blog posts for us to reformat.</p>

