  	<p class="text-left"><strong>[ DESIGN ] </strong>Due to the nature of the business and its fun clientele, Kris wanted to see bright colours and bolder uses of space on the website. We took inspiration from a business card that she showed us and used those shades of blue, pink and yellow for the logo redesign. We really liked the fonts she was using for her business card so we used the exact fonts and added the different colours to it. Since she wanted a splashier cover, we made the home page a solid blue background colour while the other pages simply had the word "Lockshop" coloured. We first created style tiles for our own reference, and then we made a mock-up on Photoshop to show Kris so that she would have an idea of what the design would look like. She did not have any objections to this so we proceeded to developing from there.</p>
     <figure>
