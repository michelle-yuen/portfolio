  	<p class="text-left"><strong>[ FUNCTIONAL DEVELOPMENT ] </strong>For the development phase, we used WordPress where we created our own custom theme and worked on both the local and live environments. Our client wanted users to be able to click on a single service and have it show the gallery directly but she didn't want to use the Lightbox plugin on WordPress. To address this, we decided to use Masonry, a JavaScript grid layout library used on sites like Pinterest and Unsplash. We also figured out how to use Isotope JS for filtering the questions and answers on the FAQ page. During this development phase, I assisted with quite a bit of the PHP, HTML, and JavaScript. We got everything to function properly before moving on to the styling.</p>
    
    <p class="text-left"><strong>[ STYLING ] </strong>This stage took about 5 days to fully complete. Since we already had the wireframes and mock-ups for our site, we simply had to follow that, except for a few minor changes along the way.</p>

    <p class="text-left"><strong>[ SITE TESTING ] </strong>A group of BCIT students from another Technical Web Designer intake participated in our site testing where we got them to complete 8 tasks on our website. After conducting the site testing, we handed in a report summarizing the methods, results, and summary.</p>

    <p class="text-left"><strong>[ TUTORING & LAUNCHING ] </strong>We scheduled a day to meet with our client to show her the final site, migrate it, and demonstrated how to use and update the site with a short tutorial.</p>
    
